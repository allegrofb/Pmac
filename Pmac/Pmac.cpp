// Pmac.cpp : 定义控制台应用程序的入口点。
//

#include "stdafx.h"
#include "PmacDeviceWnd.h"


// 唯一的应用程序对象

CWinApp theApp;

using namespace std;

int main()
{
	int nRetCode = 0;

	HMODULE hModule = ::GetModuleHandle(nullptr);

	if (hModule != nullptr)
	{
		// 初始化 MFC 并在失败时显示错误
		if (!AfxWinInit(hModule, nullptr, ::GetCommandLine(), 0))
		{
			// TODO: 更改错误代码以符合您的需要
			wprintf(L"错误: MFC 初始化失败\n");
			nRetCode = 1;
		}
		else
		{
			// TODO: 在此处为应用程序的行为编写代码。
			CPmacDeviceWnd pmac;
			pmac.run();

			fprintf(stderr, "press x key to exit\n");
			char key = std::getchar();

			while (key != 'x')
			{
				switch (key)
				{
				case 'q':
					fprintf(stderr, "I'm a test");
					break;
				case 's':
					pmac.start();
					break;
				case 't':
					pmac.stop();
					break;
				case 'd':
					pmac.get();
					break;
				default:
					break;
				}

				key = std::getchar();
			}

			pmac.quit();
		}
	}
	else
	{
		// TODO: 更改错误代码以符合您的需要
		wprintf(L"错误: GetModuleHandle 失败\n");
		nRetCode = 1;
	}

	return nRetCode;
}




